import { expect, test } from 'vitest'
import { mazeSolver } from './maze-solver'

test('maze solver', () => {
	expect(
		mazeSolver(
			[
				'##### #',
				'#     #',
				'# #####',
				'#  ####',
				'## ####',
				'##  ###',
				'###   #',
				'##  ###',
				'#  ####',
			],
			'#',
			{ x: 1, y: 8 },
			{ x: 5, y: 0 },
		),
	).toMatchInlineSnapshot(`
		[
		  {
		    "x": 1,
		    "y": 8,
		  },
		  {
		    "x": 2,
		    "y": 8,
		  },
		  {
		    "x": 2,
		    "y": 7,
		  },
		  {
		    "x": 3,
		    "y": 7,
		  },
		  {
		    "x": 3,
		    "y": 6,
		  },
		  {
		    "x": 3,
		    "y": 5,
		  },
		  {
		    "x": 2,
		    "y": 5,
		  },
		  {
		    "x": 2,
		    "y": 4,
		  },
		  {
		    "x": 2,
		    "y": 3,
		  },
		  {
		    "x": 1,
		    "y": 3,
		  },
		  {
		    "x": 1,
		    "y": 2,
		  },
		  {
		    "x": 1,
		    "y": 1,
		  },
		  {
		    "x": 2,
		    "y": 1,
		  },
		  {
		    "x": 3,
		    "y": 1,
		  },
		  {
		    "x": 4,
		    "y": 1,
		  },
		  {
		    "x": 5,
		    "y": 1,
		  },
		  {
		    "x": 5,
		    "y": 0,
		  },
		]
	`)
})
